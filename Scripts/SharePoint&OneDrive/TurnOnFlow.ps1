Param(
        [Parameter(Mandatory=$true)][String]$enviromentUrl,
        [Parameter(Mandatory=$true)][String]$username,
		[Parameter(Mandatory=$true)][String]$flowFileName
    )

$Invocation = (Get-Variable MyInvocation).Value
$DirectoryPath = (Get-Item $Invocation.MyCommand.Path).Directory.FullName
$LogPath = $DirectoryPath + "\TurnOnFlow_$([DateTime]::Now.ToString("yyyyMMddHHmmssfff")).log"
$CsvPath = $DirectoryPath + "\TurnOnFlow_$([DateTime]::Now.ToString("yyyyMMddHHmmssfff")).csv"
$flowFilePath = $DirectoryPath + "\" + $flowFileName



Function Main{
    Write-LogInfo -LogPath $LogPath -Message "Begin to run Script." -ToScreen

    CheckPowershellEnvironment

    $information = Import-Csv $flowFilePath -Header workflowidunique,workflowname

    for($i=1;$i -lt $information.Count;$i++)
    {
        $line = $information[$i]
        Write-LogInfo -LogPath $LogPath -Message "Read csv flow information: [$($line)]" -ToScreen
        TurnOnFlow $line.workflowidunique $line.workflowname
    }
    Write-LogInfo -LogPath $LogPath -Message "Finished to run Script" -ToScreen
}
Function CheckPowershellEnvironment{

    $HasPowerApp = Get-Module -ListAvailable -FullyQualifiedName "Microsoft.PowerApps.PowerShell"
    if($HasPowerApp.Count -eq 0)
    {
        try
        {
            Install-Module Microsoft.PowerApps.PowerShell -AllowClobber -Force -Scope CurrentUser
            Write-LogInfo -LogPath $LogPath -Message "Don't have Microsoft.PowerApps.PowerShell, install successful." -ToScreen
        }
        catch
        {
            Write-LogError -LogPath $LogPath -Message "Failed to install Microsoft.PowerApps.PowerShell, Message: [$($_.ToString())]" -ToScreen -Exception $_
        }
    }
    $HasXrmData = Get-Module -ListAvailable -FullyQualifiedName "Microsoft.Xrm.Data.PowerShell"
    if($HasXrmData.Count -eq 0)
    {
        try
        {
            Install-Module Microsoft.Xrm.Data.PowerShell -AllowClobber -Force -Scope CurrentUser
            Write-LogInfo -LogPath $LogPath -Message "Don't have Microsoft.Xrm.Data.PowerShell, install successful." -ToScreen
        }
        catch
        {
            Write-LogError -LogPath $LogPath -Message "Failed to install Microsoft.Xrm.Data.PowerShell, Message: [$($_.ToString())]" -ToScreen -Exception $_
        }
    }
}

Function TurnOnFlow{
    [cmdletbinding()]
    Param(
            [Parameter(Mandatory=$true)][String]$workflowidunique,
            [Parameter(Mandatory=$true)][String]$workflowname
        )

    $results = @()
    $turnOnSuccessful = New-Object PSObject
    $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "workflowidunique" -value $workflowidunique
    $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "workflowname" -value $workflowname
    try
    {
        $connection = Connect-CRMOnline -Username $username -ServerUrl $enviromentUrl

        if($connection.IsReady -ne $True)
        {
            $errorDescr = $connection.LastCrmError
            Write-LogInfo -LogPath $LogPath -Message "Connection not established: $errorDescr" -ToScreen
        }
        else
        {
            Write-LogInfo -LogPath $LogPath -Message "Connection to $($connection.ConnectedOrgFriendlyName) successful" -ToScreen

            $flow = Get-Flow -FlowName $workflowidunique
            if($flow.Internal.StatusCode -eq 404)
            {
                Write-LogError -LogPath $LogPath -Message "Could not found flow. Workflow unique id: [$($workflowidunique)], Message: [$($flow.Internal)]" -ToScreen -Exception $_
                $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Failed"
                $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value $flow.Internal.Message
            }
            elseif($flow.Internal.StatusCode -eq 403)
            {
                Write-LogError -LogPath $LogPath -Message "User does not have sufficient permissions. Workflow unique id: [$($workflowidunique)], Message: [$($flow.Internal)]" -ToScreen -Exception $_
                $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Failed"
                $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value $flow.Internal.Message
            }
            else
            {
                if($flow.Enabled -eq $False)
                {
                    try
                    {
                        Set-CrmRecordState -conn $connection -EntityLogicalName workflow -Id $flow.Internal.properties.workflowEntityId -StateCode Activated -StatusCode Activated -Verbose -Debug
                        $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Successful"
                        $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value ""
                        Write-LogInfo -LogPath $LogPath -Message "Turn on flow successful. Workflow unique id: [$($workflowidunique)]" -ToScreen
                    }
                    catch
                    {
                        Write-LogError -LogPath $LogPath -Message "An error occored when activating flow. Workflow unique id: [$($workflowidunique)], Message: [$($_.Exception.Message)]" -ToScreen -Exception $_
                        if($_.Exception.Message.IndexOf("ConnectionAuthorizationFailed") -ge 0)
                        {
                            $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Failed"
                            $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value "The connection reference cannot be used to activate this flow; it may not be a valid connection, or it may not be a connection to which you have access. Either replace the connection with a valid connection that you can access, or have the connection owner activate the flow to share the connection with you in the context of this flow. For details, please check the log."
                        }
                        elseif($_.Exception.Message.IndexOf("XrmConnectionReferenceMissingConnection") -ge 0)
                        {
                            $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Failed"
                            $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value "Connection Reference lacks connection ID, please check whether the Connection Reference is correctly connected. For details, please check the log."
                        }
                        else
                        {
                            $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Failed"
                            $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value $_.Exception.Message
                        }
                    }
                }
                else
                {
                    $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Successful"
                    $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value ""
                    Write-LogInfo -LogPath $LogPath -Message "Flow has been turned on and does not need to be turned on again. Workflow unique id: [$($workflowidunique)]" -ToScreen
                }
            }
        }

        $results += $turnOnSuccessful
    }
    catch
    {
        Write-LogError -LogPath $LogPath -Message "Failed to turn on flow, Workflow unique id: [$($workflowidunique)], Message: [$($_.ToString())]" -ToScreen -Exception $_
        $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Turn on Status" -value "Failed"
        $turnOnSuccessful | Add-Member -MemberType NoteProperty -name "Error Message" -value $_.ToString()
        $results += $turnOnSuccessful
    }
    $results | Export-Csv -Path $CsvPath -NoTypeInformation -Encoding UTF8 -Append
}
Function Write-LogInfo
{
    [CmdletBinding()]
    Param (
        [Parameter(Mandatory=$true,Position=0)][string]$LogPath,
        [Parameter(Mandatory=$true,Position=1,ValueFromPipeline=$true)][string]$Message,
        [Parameter(Mandatory=$false,Position=2)][switch]$ToScreen
    )

    Process 
    {
        $Message = "INFO:  [$([DateTime]::Now.ToString("yyyy-MM-dd HH:mm:ss:fff"))] $Message  "

        Add-Content -Path $LogPath -Value $Message

        Write-Debug $Message

        If ( $ToScreen -eq $True ) 
        {
            Write-Host -ForegroundColor Green $Message
        }
    }
}

Function Write-LogError
{
    [CmdletBinding()]
    Param (
        [Parameter(Mandatory=$true,Position=0)][string]$LogPath,
        [Parameter(Mandatory=$true,Position=1,ValueFromPipeline=$true)][string]$Message,
        [Parameter(Mandatory=$false,Position=2)][switch]$ToScreen,
        [Parameter(Mandatory=$false,Position=3)][object]$Exception

    )

    Process 
    {
        $Message = "ERROR:  [$([DateTime]::Now.ToString("yyyy-MM-dd HH:mm:ss:fff"))] $Message  "

        Add-Content -Path $LogPath -Value $Message

        Write-Debug $Message

        If ( $ToScreen -eq $True ) 
        {
            Write-Host -ForegroundColor Red $Message
        }
    }
}
